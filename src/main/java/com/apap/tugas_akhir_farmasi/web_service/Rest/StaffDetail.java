package com.apap.tugas_akhir_farmasi.web_service.Rest;

public class StaffDetail {
	
	private long id;
	private String nama;
	private int jenis;
	
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public String getNama() {
		return nama;
	}
	public void setNama(String nama) {
		this.nama = nama;
	}
	public int getJenis() {
		return jenis;
	}
	public void setJenis(int jenis) {
		this.jenis = jenis;
	}
	
}
